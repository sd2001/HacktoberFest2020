# Python Program to explain math.prod() method 

# Importing math module 
import math 

# list 
arr = [1, 2, 3, 4, 5] 

# Calculate the product of 
# of all elements present 
# in the given list 
product = math.prod(arr) 
print(product) 


# tuple 
tup = (0.5, 0.6, 0.7) 

# Calculate the product 
# of all elements present 
# in the given tuple 
product = math.prod(tup) 
print(product) 


# range 
seq = range(1, 11) 

# Calculate the product 
# of all elements present 
# in the given range 
product = math.prod(seq) 
print(product) 

